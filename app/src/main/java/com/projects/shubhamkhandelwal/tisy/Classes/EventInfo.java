public class EventInfo {
    private String sLocation;
    private String sLocationDesc;
    private String dLocation;
    private String dLocationDesc;

    public EventInfo(){}
    public String getsLocation() {
        return sLocation;
    }

    public void setsLocation(String sLocation) {
        this.sLocation = sLocation;
    }

    public String getsLocationDesc() {
        return sLocationDesc;
    }

    public void setsLocationDesc(String sLocationDesc) {
        this.sLocationDesc = sLocationDesc;
    }

    public String getdLocation() {
        return dLocation;
    }

    public void setdLocation(String dLocation) {
        this.dLocation = dLocation;
    }

    public String getdLocationDesc() {
        return dLocationDesc;
    }

    public void setdLocationDesc(String dLocationDesc) {
        this.dLocationDesc = dLocationDesc;
    }


}
